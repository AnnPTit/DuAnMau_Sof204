    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        QlCuaHang cuaHang = validateCuaHang();
        if (cuaHang == null) {
            return;
        }
        if (list.size() != 0) {
            for (QlCuaHang qlCuaHang : list) {
                if (qlCuaHang.getMa().equalsIgnoreCase(cuaHang.getMa())) {
                    JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                }
            }
        }
        if (iManagerCuaHangService.createNewCuaHang(cuaHang) != null) {
            JOptionPane.showMessageDialog(this, "Thành Công");

        } else {
            JOptionPane.showMessageDialog(this, "Thất Bại");
        }
        loadTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tblCuaHang.getSelectedRow();
        if (row != -1) {
            String ma = (String) model.getValueAt(row, 0);
            if (iManagerCuaHangService.deleteCuaHang(ma) != 0) {
                JOptionPane.showMessageDialog(this, "Thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Thất Bại");
            }
            loadTable();
        } else {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần xóa");
            return;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        QlCuaHang qlCuaHang = validateCuaHang();
        if (qlCuaHang == null) {
            return;
        }
        if (iManagerCuaHangService.updateCuaHang(qlCuaHang) != null) {
            JOptionPane.showMessageDialog(this, "Thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Thất Bại");
        }
        loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

