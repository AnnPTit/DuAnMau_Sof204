    private void loadTable() {
        list = new ArrayList<>();
        list = iManagerCuaHangService.getAllCuaHang();
        model.setNumRows(0);
        for (QlCuaHang qlCuaHang : list) {
            model.addRow(new Object[]{
                qlCuaHang.getMa(),
                qlCuaHang.getTen(),
                qlCuaHang.getDiaChi(),
                qlCuaHang.getThanhPho(),
                qlCuaHang.getQuocGia()});
        }
    }

    private QlCuaHang validateCuaHang() {
        String maCh = txtMa.getText();
        boolean isValid = true;
        if (maCh.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã trống");
            isValid = false;
            return null;
        }
        String ten = txtTen.getText();
        if (ten.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên trống");
            isValid = false;
            return null;
        }
        String diaChi = txtDiaChi.getText();
        if (diaChi.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Địa chỉ trống");
            isValid = false;
            return null;
        }
        String tp = txtTP.getText();
        if (tp.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Thành phố trống");
            isValid = false;
            return null;
        }
        String quocGia = txtQuocGia.getText();
        if (quocGia.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Quốc gia trống");
            isValid = false;
            return null;
        }
        if (isValid == true) {
            QlCuaHang qlCuaHang = new QlCuaHang(maCh, ten, diaChi, tp, quocGia);
            return qlCuaHang;
        }
        return null;
    }

