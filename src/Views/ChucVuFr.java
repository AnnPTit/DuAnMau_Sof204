    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        QLChucVu newChucVu = getChuVuFromInput();
        if (newChucVu == null) {
            return;
        }
//        if (list.size() != 0) {
//            if (_iIManagerChucVuService.getChucVuById(newChucVu.getMa()) != null) {
//                JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
//                return;
//            }
//        }
        if (list.size() != 0) {
            for (QLChucVu qLChucVu : list) {
                if (qLChucVu.getMa().equalsIgnoreCase(newChucVu.getMa())) {
                    JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                    return;
                }
            }
        }

        if (_iIManagerChucVuService.createNewChucVu(newChucVu) != null) {
            JOptionPane.showMessageDialog(this, "Thành Công");
        } else { 
            JOptionPane.showMessageDialog(this, "Thất bại");

        }
        loadTable();

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        QLChucVu chucVu = getChuVuFromInput();
        if (chucVu == null) {
            return;
        }
        if (_iIManagerChucVuService.updateChuVu(chucVu) != null) {
            JOptionPane.showMessageDialog(this, "Thành Công");
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
        loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        if (ma.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nhập mã cần xóa", "error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (_iIManagerChucVuService.deleteChucVu(ma) != 0) {
            JOptionPane.showMessageDialog(this, "Thành Công");
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
        loadTable();


    }//GEN-LAST:event_btnXoaActionPerformed

