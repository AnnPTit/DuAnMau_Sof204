    private void loadTable() {
        list = _iIManagerChucVuService.getChucVu();
        model.setNumRows(0);
        for (QLChucVu x : list) {
            model.addRow(new Object[]{
                x.getMa(),
                x.getTen()
            });
        }

    }

    private QLChucVu getChuVuFromInput() {
        QLChucVu qLChucVu = new QLChucVu();
        String ma = txtMa.getText();
        if (ma.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã trống");
            return null;
        }
        qLChucVu.setMa(ma);

        String ten = txtTen.getText();
        if (ten.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên trống");
            return null;
        }
        qLChucVu.setTen(ten);

        return qLChucVu;
    }

